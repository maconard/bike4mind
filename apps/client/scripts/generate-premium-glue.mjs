#!/usr/bin/env node
/**
 * Premium overlay codegen.
 *
 * Discovers packages under packages/premium/ by reading their package.json
 * b4mContributions field, then generates glue files into apps/client:
 *
 *   app/premium-generated/premiumRoutes.generated.ts   - Tanstack SPA routes
 *   app/premium-generated/premiumNavItems.generated.ts  - nav/HUD slots
 *   app/premium-generated/premiumNotebookSidenav.generated.ts - notebook sidenav slot
 *   pages/api/<stub>.ts (per-package)                  - Next.js API stubs
 *   server/premium-generated/<stub>.ts (per-package)   - SST Lambda handler stubs
 *   server/premium-generated/premiumLlmTools.generated.ts - LLM tool contributions
 *
 * Two distinct "empty" forms when no premium packages are present:
 *   SPA/nav   -> emit the file with an empty exported array
 *   API/handler stubs -> emit ZERO files (no file = no import of absent package)
 *
 * Run by: pnpm codegen (or automatically via predev/prebuild scripts)
 * Turbo cache key: packages/premium/*\/package.json
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync, rmSync } from 'node:fs';
import { resolve, dirname, join, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const CLIENT_ROOT = resolve(__dirname, '..');
const REPO_ROOT = resolve(CLIENT_ROOT, '../..');
const PREMIUM_DIR = join(REPO_ROOT, 'packages/premium');
const GENERATED_DIR = join(CLIENT_ROOT, 'app/premium-generated');
const INFRA_GENERATED_DIR = join(REPO_ROOT, 'infra/premium-generated');

// --- Discover premium packages ---

function discoverPremiumPackages() {
  if (!existsSync(PREMIUM_DIR)) return [];

  const packages = [];
  for (const entry of readdirSync(PREMIUM_DIR, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const pkgPath = join(PREMIUM_DIR, entry.name, 'package.json');
    if (!existsSync(pkgPath)) continue;
    try {
      const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
      if (pkg.b4mContributions) {
        packages.push({ name: pkg.name, dir: entry.name, contributions: pkg.b4mContributions });
      }
    } catch {
      // malformed package.json - skip
    }
  }
  return packages;
}

// --- Generate helpers ---

const GENERATED_BANNER = `// AUTO-GENERATED by scripts/generate-premium-glue.mjs — do not edit.
// Re-run: pnpm codegen
// This file is gitignored. It is regenerated on every build and dev server start.
`;

function ensureDir(dir) {
  mkdirSync(dir, { recursive: true });
}

function writeFile(path, content) {
  ensureDir(dirname(path));
  writeFileSync(path, content, 'utf8');
  console.log(`  wrote ${path.replace(REPO_ROOT + '/', '')}`);
}

// A bare ES module specifier: package name, optional scope, optional subpath.
// These values are interpolated raw into generated `import ... from '<spec>'`,
// so reject anything with quotes/semicolons/whitespace that could break out of
// the string literal and inject code. First-party today, but the seam is
// designed for third-party-ish packages to self-describe (b4mContributions).
// NOTE: the char class includes `.`, so the regex alone still matches `..` and
// `.` path segments (e.g. "../../x", "foo/../bar") - a relative import, which is
// exactly what a *bare* specifier must not be. Reject those segments explicitly.
const MODULE_SPECIFIER_RE = /^(@[a-z0-9._-]+\/)?[a-z0-9._-]+(\/[a-z0-9._-]+)*$/i;

function assertModuleSpecifier(value, pkgName, field) {
  if (typeof value !== 'string' || !MODULE_SPECIFIER_RE.test(value)) {
    throw new Error(
      `[codegen] invalid ${field} from package "${pkgName}": ` +
        `${JSON.stringify(value)} is not a bare module specifier`
    );
  }
  if (value.split('/').some(seg => seg === '..' || seg === '.')) {
    throw new Error(
      `[codegen] invalid ${field} from package "${pkgName}": ` +
        `${JSON.stringify(value)} contains a relative-path segment (. or ..)`
    );
  }
}

// --- Generate SPA routes ---

function generateSpaRoutes(packages) {
  const outPath = join(GENERATED_DIR, 'premiumRoutes.generated.ts');
  // Core-owned descriptor type. Annotating BOTH forms with it (rather than
  // letting the empty form fall back to `unknown[]`) keeps router.tsx's
  // consumption type-stable in the open-core fork - an untyped empty array
  // collapses the Tanstack route-tree types and cascades errors into unrelated
  // routes. The premium package's own descriptor must be assignable to it.
  const typeImport = `import type { PremiumRouteDescriptor } from '../premiumContract';`;

  if (packages.length === 0) {
    writeFile(
      outPath,
      `${GENERATED_BANNER}\n${typeImport}\n\nexport const premiumRoutes: PremiumRouteDescriptor[] = [];\n`
    );
    return;
  }

  const contributors = packages.filter(p => p.contributions.spaRoutesExport);
  contributors.forEach(p => assertModuleSpecifier(p.contributions.spaRoutesExport, p.name, 'spaRoutesExport'));

  const imports = contributors
    .map((p, i) => `import { spaRoutes as routes${i} } from '${p.contributions.spaRoutesExport}';`)
    .join('\n');

  const exports = contributors.map((_, i) => `  ...routes${i}`).join(',\n');

  writeFile(
    outPath,
    `${GENERATED_BANNER}\n${typeImport}\n${imports}\n\nexport const premiumRoutes: PremiumRouteDescriptor[] = [\n${exports}\n];\n`
  );
}

// --- Generate nav items ---

function generateNavItems(packages) {
  const outPath = join(GENERATED_DIR, 'premiumNavItems.generated.ts');
  // Same annotate-both-forms rule as routes: the core-owned contract keeps the
  // consumer's types identical whether the overlay is present or empty.
  const typeImport = `import type { PremiumNavDescriptor } from '../premiumContract';`;

  if (packages.length === 0) {
    writeFile(
      outPath,
      `${GENERATED_BANNER}\n${typeImport}\n\nexport const premiumNavItems: PremiumNavDescriptor[] = [];\n`
    );
    return;
  }

  const contributors = packages.filter(p => p.contributions.navItemsExport);
  contributors.forEach(p => assertModuleSpecifier(p.contributions.navItemsExport, p.name, 'navItemsExport'));

  const imports = contributors
    .map((p, i) => `import { navItems as items${i} } from '${p.contributions.navItemsExport}';`)
    .join('\n');

  const exports = contributors.map((_, i) => `  ...items${i}`).join(',\n');

  writeFile(
    outPath,
    `${GENERATED_BANNER}\n${typeImport}\n${imports}\n\nexport const premiumNavItems: PremiumNavDescriptor[] = [\n${exports}\n];\n`
  );
}

// --- Generate notebook sidenav slot ---

// A premium package can contribute a full-surface sidenav body that REPLACES the
// default notebook sidenav on its own appShell route (b4mContributions.
// notebookSidenavExport -> a module default-exporting the component). Core STATICALLY
// imports this generated module (same annotate-both-forms rule as routes/nav), so the
// premium package specifier must live ONLY here - the present form names it, the fork
// emits the `null` form and never references the absent package. A single slot: the
// notebook shell shows one premium sidenav per route, so if multiple packages declare
// one, the first wins (with a warning).
function generateNotebookSidenav(packages) {
  const outPath = join(GENERATED_DIR, 'premiumNotebookSidenav.generated.ts');
  const typeImport = `import type { PremiumNotebookSidenav } from '../premiumContract';`;

  const contributors = packages.filter(p => p.contributions.notebookSidenavExport);
  contributors.forEach(p =>
    assertModuleSpecifier(p.contributions.notebookSidenavExport, p.name, 'notebookSidenavExport')
  );

  if (contributors.length === 0) {
    writeFile(
      outPath,
      `${GENERATED_BANNER}\n${typeImport}\n\nexport const premiumNotebookSidenav: PremiumNotebookSidenav = null;\n`
    );
    return;
  }

  if (contributors.length > 1) {
    console.warn(
      `[codegen] WARNING: multiple packages declare b4mContributions.notebookSidenavExport ` +
        `(${contributors.map(p => p.name).join(', ')}); using the first (${contributors[0].name}).`
    );
  }

  const spec = contributors[0].contributions.notebookSidenavExport;
  writeFile(
    outPath,
    `${GENERATED_BANNER}
import dynamic from 'next/dynamic';
${typeImport}

// Source: ${contributors[0].name} via b4mContributions.notebookSidenavExport
// Lazy-loaded (ssr: false) so the premium surface's bundle stays out of every other route.
export const premiumNotebookSidenav: PremiumNotebookSidenav = dynamic(
  () => import('${spec}').then(m => ({ default: m.default })),
  { ssr: false }
);
`
  );
}

// --- Generate API route stubs ---

// Resolve a validated bare specifier (e.g. '@bike4mind/premium-optihashi/api/webhooks/qwork')
// declared by `pkg` back to the real source file on disk, via that package's own
// `exports` map in its package.json - so we can inspect the source without executing it.
// Returns null (never throws) for any shape this doesn't confidently recognize;
// callers treat null as "no config to re-export", which is the pre-existing behavior.
function resolveExportFromToSourceFile(pkg, exportFrom) {
  let subpath;
  if (exportFrom === pkg.name) subpath = '.';
  else if (exportFrom.startsWith(`${pkg.name}/`)) subpath = `.${exportFrom.slice(pkg.name.length)}`;
  else return null; // exportFrom names a different package - not resolvable from here.

  const pkgJsonPath = join(PREMIUM_DIR, pkg.dir, 'package.json');
  let pkgJson;
  try {
    pkgJson = JSON.parse(readFileSync(pkgJsonPath, 'utf8'));
  } catch {
    return null;
  }
  const target = pkgJson.exports?.[subpath];
  if (target === undefined) return null; // no exports entry for this subpath - nothing to resolve.
  if (typeof target !== 'string') {
    // A conditional-exports object ({ import, require, ... }) or array target exists at this
    // key but isn't a plain string path we can read - warn rather than silently returning null,
    // matching extractConfigLiteral's posture: an unresolvable source file means any `config`
    // export it might have is silently dropped from the generated stub, which can reintroduce
    // the exact bodyParser hang this file exists to prevent.
    console.warn(
      `[codegen] WARNING: ${pkg.name}'s package.json exports["${subpath}"] is not a plain string ` +
        `path (got ${JSON.stringify(target)}) - can't resolve it to check for a "config" export. ` +
        `If this route needs Next's bodyParser config, the generated stub will be missing it.`
    );
    return null;
  }

  // Containment guard, mirroring the ones below for generatedPath (:293, :349):
  // `target` comes from the overlay's own package.json, which is already fully
  // trusted (its .ts source is compiled directly into the app elsewhere), but stay
  // consistent with this file's own defense-in-depth convention regardless of that.
  const pkgRoot = resolve(join(PREMIUM_DIR, pkg.dir));
  const resolved = resolve(join(PREMIUM_DIR, pkg.dir, target));
  if (resolved !== pkgRoot && !resolved.startsWith(pkgRoot + sep)) return null;

  return existsSync(resolved) ? resolved : null;
}

// Next.js's Pages Router requires `export const config = {...}` to be a literal,
// statically-analyzable object declared DIRECTLY in the route file - a bare
// `export { config } from 'source'` re-export is not reliably picked up by Next's
// build-time page-config analyzer. Dropping `config` silently re-enables Next's
// default body parser, which then consumes a raw-body handler's request stream
// before the handler's own manual read ever runs (verified live: a correctly-signed
// webhook POST to a route missing this hung forever instead of getting a response).
// So instead of re-exporting `config` across a module boundary, copy its literal
// text into the generated glue file so Next analyzes it directly, in-file.
//
// The literal-extraction regex requires the closing `}` to be immediately followed
// by `;` (no space) - true for this repo's own Prettier-formatted source today, but
// a differently-formatted config would silently fail to extract and fall back to no
// config export at all, i.e. reintroducing the exact hang this function exists to
// prevent. Warn loudly in that case instead of failing silently.
function extractConfigLiteral(sourceFilePath) {
  if (!sourceFilePath) return null;
  let content;
  try {
    content = readFileSync(sourceFilePath, 'utf8');
  } catch {
    return null;
  }
  // Anchored to line start (with leading whitespace allowed) so a config-shaped literal
  // sitting inside a comment or string above the real declaration can't win the match -
  // otherwise `.match()` would still succeed there, skipping the fallback warning below
  // while emitting whatever that comment/string literal happened to contain. Tolerates an
  // optional type annotation (`config: PageConfig =`) between the name and `=` - without
  // it, an annotated declaration matches neither this regex nor the fallback trigger below,
  // silently emitting no config export with no warning (no route currently uses this form,
  // but nothing stops one from starting to).
  const match = content.match(/^\s*export\s+const\s+config(?::\s*[A-Za-z_][A-Za-z0-9_.]*)?\s*=\s*(\{[\s\S]*?\});/m);
  if (match) return match[1];
  if (/export\s+const\s+config(?::\s*[A-Za-z_][A-Za-z0-9_.]*)?\s*=/.test(content)) {
    console.warn(
      `[codegen] WARNING: ${sourceFilePath} declares "export const config" but its literal object ` +
        `could not be extracted (unexpected formatting) - the generated stub will be missing this ` +
        `config, which can silently reintroduce a bodyParser-related hang.`
    );
  }
  return null;
}

function generateApiStubs(packages) {
  // Wipe all previously-generated stubs so removing a package removes its stubs.
  // Pattern: pages/api/premium-* dirs (all premium API dirs are namespaced premium-*).
  const pagesApiDir = join(CLIENT_ROOT, 'pages/api');
  if (existsSync(pagesApiDir)) {
    for (const entry of readdirSync(pagesApiDir, { withFileTypes: true })) {
      if (entry.isDirectory() && entry.name.startsWith('premium-')) {
        rmSync(join(pagesApiDir, entry.name), { recursive: true, force: true });
        console.log(`  removed ${`pages/api/${entry.name}`}`);
      }
    }
  }

  // No packages -> zero stubs (the "absent" form for API routes).
  const pagesApiRoot = resolve(pagesApiDir);
  for (const pkg of packages) {
    if (!pkg.contributions.apiRouteStubs) continue;
    for (const stub of pkg.contributions.apiRouteStubs) {
      const { generatedPath, exportFrom } = stub;

      // Containment guard. The wipe loop above only removes *directories* named
      // `premium-*` directly under pages/api, and .gitignore only matches
      // `pages/api/premium-*/`. So every generated stub MUST live inside such a
      // directory, or it escapes both the wipe (orphaned on package removal)
      // and the ignore (accidentally committable). A bare prefix check is NOT
      // enough: it passes `..` segments too (e.g. "pages/api/premium-x/../../evil.ts"
      // resolves outside the client root). Resolve first, then assert:
      //   (1) the resolved path stays strictly inside pages/api, and
      //   (2) its first segment under pages/api is a `premium-*` DIRECTORY
      //       (i.e. there is a further path segment - not a bare file).
      const outPath = resolve(join(CLIENT_ROOT, generatedPath));
      const rel = outPath.startsWith(pagesApiRoot + sep) ? outPath.slice(pagesApiRoot.length + 1) : null;
      const firstSeg = rel?.split(sep)[0] ?? '';
      if (!rel || !firstSeg.startsWith('premium-') || !rel.includes(sep)) {
        throw new Error(
          `[codegen] unsafe generatedPath from package "${pkg.name}": ` +
            `${JSON.stringify(generatedPath)} must resolve inside a ` +
            `pages/api/premium-*/ directory`
        );
      }

      // exportFrom is interpolated raw into `export { default } from '<spec>'`.
      assertModuleSpecifier(exportFrom, pkg.name, 'apiRouteStubs.exportFrom');

      // If the source route exports a Next.js page `config` (e.g. { api: { bodyParser:
      // false } } for a raw-body webhook handler), copy its literal text in - see
      // extractConfigLiteral's comment for why this can't just be re-exported.
      const configLiteral = extractConfigLiteral(resolveExportFromToSourceFile(pkg, exportFrom));
      const configExport = configLiteral ? `export const config = ${configLiteral};\n` : '';

      writeFile(
        outPath,
        `${GENERATED_BANNER}// Source: ${pkg.name} via b4mContributions.apiRouteStubs\nexport { default } from '${exportFrom}';\n${configExport}`
      );
    }
  }
}

// --- Generate server handler stubs ---

// SST Lambda handlers (crons, queue handlers) for premium products live in the
// premium package, but SST references them by a stable apps/client path. These
// stubs re-export the package handlers into server/premium-generated/ so the
// path is stable AND the fork (no premium package) simply omits them - a
// committed static import of an absent package would be a fatal typecheck error.
function generateServerHandlerStubs(packages) {
  const serverGeneratedDir = join(CLIENT_ROOT, 'server/premium-generated');
  const serverGeneratedRoot = resolve(serverGeneratedDir);

  // Wipe previously-generated stubs so removing a package removes its stubs.
  // The whole dir is generated (namespaced like app/premium-generated/), so a
  // clean rm is safe and mirrors the API-stub wipe.
  if (existsSync(serverGeneratedDir)) {
    rmSync(serverGeneratedDir, { recursive: true, force: true });
    console.log(`  removed ${'server/premium-generated'}`);
  }

  // No packages -> zero stubs (the "absent" form).
  for (const pkg of packages) {
    if (!pkg.contributions.serverHandlerStubs) continue;
    for (const stub of pkg.contributions.serverHandlerStubs) {
      const { generatedPath, exportFrom } = stub;

      // Containment guard: the resolved path must stay strictly inside
      // server/premium-generated/ so the wipe above fully governs it and no
      // `..` segment can escape into real source dirs.
      const outPath = resolve(join(CLIENT_ROOT, generatedPath));
      if (outPath !== serverGeneratedRoot && !outPath.startsWith(serverGeneratedRoot + sep)) {
        throw new Error(
          `[codegen] unsafe generatedPath from package "${pkg.name}": ` +
            `${JSON.stringify(generatedPath)} must resolve inside server/premium-generated/`
        );
      }

      // exportFrom is interpolated raw into `export * from '<spec>'`. `export *`
      // forwards all named exports (handler, dispatch, ...) that SST references.
      assertModuleSpecifier(exportFrom, pkg.name, 'serverHandlerStubs.exportFrom');

      writeFile(
        outPath,
        `${GENERATED_BANNER}// Source: ${pkg.name} via b4mContributions.serverHandlerStubs\nexport * from '${exportFrom}';\n`
      );
    }
  }
}

// --- Generate LLM tool contributions ---

// Premium packages can contribute LLM tool implementations (b4mContributions.
// llmToolsExport pointing at a module exporting `llmTools: Record<string,
// ToolDefinition>`). Unlike API/handler stubs, chat entrypoints STATICALLY
// import this file and pass the map to ChatCompletionProcess as
// `externalTools`, so it must exist in BOTH forms (same annotate-both-forms
// rule as routes/nav): present -> aggregated spread of each package's map;
// absent -> empty object typed with the core-owned ToolDefinition contract.
// Written after generateServerHandlerStubs, whose wipe clears the whole
// server/premium-generated/ dir.
function generateLlmTools(packages) {
  const outPath = join(CLIENT_ROOT, 'server/premium-generated/premiumLlmTools.generated.ts');
  const typeImport = `import type { ToolDefinition } from '@bike4mind/services';`;

  const contributors = packages.filter(p => p.contributions.llmToolsExport);

  if (contributors.length === 0) {
    writeFile(
      outPath,
      `${GENERATED_BANNER}\n${typeImport}\n\nexport const premiumLlmTools: Record<string, ToolDefinition> = {};\n`
    );
    return;
  }

  contributors.forEach(p => assertModuleSpecifier(p.contributions.llmToolsExport, p.name, 'llmToolsExport'));

  const imports = contributors
    .map((p, i) => `import { llmTools as tools${i} } from '${p.contributions.llmToolsExport}';`)
    .join('\n');

  const spreads = contributors.map((_, i) => `  ...tools${i}`).join(',\n');

  writeFile(
    outPath,
    `${GENERATED_BANNER}\n${typeImport}\n${imports}\n\nexport const premiumLlmTools: Record<string, ToolDefinition> = {\n${spreads}\n};\n`
  );
}

// --- Generate infra glue ---

// For each premium package that declares `b4mContributions.infra`, emit a thin
// glue file at infra/premium-generated/<dir>-infra.generated.ts so sst.config.ts
// can import a stable path instead of the hard-coded overlay source path.
//
// TWO forms:
//   overlay PRESENT -> re-export contributeInfra from the package source via a
//     RELATIVE import. Bare specifiers (e.g. @bike4mind/premium-overwatch/infra)
//     cannot be used here: esbuild (which `pnpm sst install` uses to bundle
//     sst.config.ts) externalises symlinked workspace packages (a known
//     evanw/esbuild limitation), leaving the specifier un-bundled and failing at runtime.
//   overlay ABSENT -> export a no-op stub so sst.config.ts can import and call
//     contributeInfra() without the overlay present (open-core fork / CI fork-sim).
//
// The infra/ dir is outside apps/client/ so INFRA_GENERATED_DIR goes up to repo
// root; REPO_ROOT is already derived from CLIENT_ROOT above.

// Parse sst.config.ts to derive which infra overlay dirs are expected - this
// avoids a separate manifest to keep in sync. Any line matching the import pattern
// `infra/premium-generated/<dir>-infra.generated` is an expected glue file, so
// codegen can always generate a no-op for absent overlays, even on first run.
function getExpectedInfraOverlayDirs() {
  const sstConfigPath = join(REPO_ROOT, 'sst.config.ts');
  if (!existsSync(sstConfigPath)) return [];
  const content = readFileSync(sstConfigPath, 'utf8');
  const matches = [...content.matchAll(/infra\/premium-generated\/([a-z0-9-]+)-infra\.generated/g)];
  return [...new Set(matches.map(m => m[1]))];
}

function generateInfraGlue(packages) {
  ensureDir(INFRA_GENERATED_DIR);

  // Build a map of dir -> package for present overlays that declare infra.
  const presentByDir = new Map();
  for (const pkg of packages) {
    if (pkg.contributions.infra) presentByDir.set(pkg.dir, pkg);
  }

  // Ensure every dir imported by sst.config.ts has a glue file, whether the
  // overlay is present (re-export) or absent (no-op). This is safe on a fresh
  // CI checkout where infra/premium-generated/ has never been populated.
  const expectedDirs = getExpectedInfraOverlayDirs();
  for (const dir of expectedDirs) {
    const outPath = join(INFRA_GENERATED_DIR, `${dir}-infra.generated.ts`);

    if (presentByDir.has(dir)) {
      // Relative path from infra/premium-generated/ up to the overlay source.
      const relSource = `../../packages/premium/${dir}/src/infra`;
      writeFile(
        outPath,
        `${GENERATED_BANNER}
// Relative import: esbuild (used by \`pnpm sst install\`) externalises symlinked
// workspace packages (evanw/esbuild#2483), so a bare @bike4mind/premium-* specifier
// would fail to bundle. A relative .ts source import is bundled+transpiled inline.
export { contributeInfra } from '${relSource}';
export type { ContributeInfraParams } from '${relSource}';
`
      );
    } else {
      writeFile(
        outPath,
        `${GENERATED_BANNER}
// Open-core no-op: ${dir} overlay is absent. Provides a callable stub so
// sst.config.ts can import and call contributeInfra() without the overlay present.
// Record<string, unknown> (not the overlay's ContributeInfraParams) is intentional —
// the stub must compile without the overlay's types when the overlay is absent.
// eslint-disable-next-line @typescript-eslint/no-unused-vars
export function contributeInfra(_params: Record<string, unknown>): void {}
`
      );
    }
  }

  // Warn for any overlay that declares infra contributions but whose dir is not
  // imported in sst.config.ts - its infra would be silently skipped otherwise.
  for (const [dir] of presentByDir) {
    if (!expectedDirs.includes(dir)) {
      console.warn(
        `[codegen] WARNING: packages/premium/${dir} declares b4mContributions.infra ` +
        `but sst.config.ts has no matching import — infra contributions will not be wired. ` +
        `Add: await import('./infra/premium-generated/${dir}-infra.generated')`
      );
    }
  }
}

// --- Main ---

const packages = discoverPremiumPackages();
console.log(`[codegen] found ${packages.length} premium package(s): ${packages.map(p => p.name).join(', ') || '(none)'}`);

ensureDir(GENERATED_DIR);
generateSpaRoutes(packages);
generateNavItems(packages);
generateNotebookSidenav(packages);
generateApiStubs(packages);
generateServerHandlerStubs(packages);
generateLlmTools(packages);
generateInfraGlue(packages);

console.log('[codegen] done.');
